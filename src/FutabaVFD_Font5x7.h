/*
  FutabaVFD_Font5x7.h - Embedded 5x7 pixel font for vertical-scroll
  animations on the M66004-class controller.

  Encoding: 5 bytes per glyph, COLUMN-MAJOR.
    byte[0] = leftmost column
    byte[4] = rightmost column
  Within each column byte:
    bit 0 (LSB) = top pixel
    bit 6       = bottom pixel
    bit 7       = unused (don't-care, datasheet section 2.2)

  This matches the CGRAM data layout exactly, so glyph upload is just a
  5-byte burst per slot - no transposition required.

  Vertical motion is therefore a per-column shift:
    column << n   moves pixels DOWN  by n
    column >> n   moves pixels UP    by n
  Both are masked to the lower 7 bits afterwards.

  Coverage: ASCII 0x20..0x7E plus the device's umlaut/degree codes
  (Ä Ö Ü ä ö ü °). Glyphs for non-printable codes are blank.

  Font shapes are minimalist 5x7 — readable but not stylised. Roughly
  500 bytes total.
*/

