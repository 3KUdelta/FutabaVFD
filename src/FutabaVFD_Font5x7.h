/*
  FutabaVFD_Font5x7.h  —  Dual font for 8-digit and 16-digit VFD modules
  =========================================================================
  5x7 pixel font for CGRAM animations on the M66004-class VFD controller.

  Two separate font tables are included:
    kAscii16[95][5]  —  16-digit display (verified against CGROM output)
    kAscii8[95][5]   —   8-digit display (verified against CGROM output)

  glyphFor(code, is16digit) selects the correct table at runtime.
  Both tables are always compiled in (~1900 bytes total flash, negligible
  on ESP32 with 4 MB flash).

  Encoding: 5 bytes per glyph, COLUMN-MAJOR.
    byte[0] = leftmost column (col 1), byte[4] = rightmost column (col 5)
    bit 0 (LSB) = top pixel (row 0), bit 6 = bottom pixel (row 6), bit 7 = unused

  Vertical shift: col >> n = UP (top disappears), col << n = DOWN (bottom disappears)
  flipIn:  offset = step-7 (-7..0), text enters row6-first from above.
  flipOut: offset = -step  (0..-7), text slides downward out of view.

  Coverage: ASCII 0x20..0x7E (95 glyphs) + German umlauts + degree sign.
*/

