inline const uint8_t* glyphFor(uint8_t code, bool is16digit) {
  if (code >= 0x20 && code <= 0x7E)
    return is16digit ? kAscii16[code - 0x20] : kAscii8[code - 0x20];

  if (is16digit) {
    switch (code) {
      case 0xC4: return kAUml;   case 0xD6: return kOUml;
      case 0xDC: return kUUml;   case 0xE4: return kaUml;
      case 0xF6: return koUml;   case 0xFC: return kuUml;
      case 0xB0: return kDegree;
    }
  } else {
    switch (code) {
      case 0x8E: return kAUml;   case 0x99: return kOUml;
      case 0x9A: return kUUml;   case 0x84: return kaUml;
      case 0x94: return koUml;   case 0x81: return kuUml;
      case 0xEF: return kDegree;
    }
  }
  return kBlank;
}

inline void readGlyph(const uint8_t* p, uint8_t out[5]) {
  for (uint8_t i = 0; i < 5; ++i) out[i] = pgm_read_byte(p + i);
}

