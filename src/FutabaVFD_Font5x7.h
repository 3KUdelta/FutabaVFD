// ---------------------------------------------------------------------
//  Glyph lookup. Returns a pointer to 5 PROGMEM bytes for the given
//  device codepoint (NOT raw ASCII — the device codepoint produced by
//  FutabaVFD::translateUtf8 is what callers should pass).
// ---------------------------------------------------------------------
inline const uint8_t* glyphFor(uint8_t code, bool is16digit) {
  if (code >= 0x20 && code <= 0x7E) {
    return kAscii[code - 0x20];
  }

  // The umlaut/degree codes are different between the 8 and 16-digit
  // device codepages. Match the table that translateUtf8() produces.
  if (is16digit) {
    switch (code) {
      case 0xC4: return kAUml;
      case 0xD6: return kOUml;
      case 0xDC: return kUUml;
      case 0xE4: return kaUml;
      case 0xF6: return koUml;
      case 0xFC: return kuUml;
      case 0xB0: return kDegree;
    }
  } else {
    switch (code) {
      case 0x8E: return kAUml;
      case 0x99: return kOUml;
      case 0x9A: return kUUml;
      case 0x84: return kaUml;
      case 0x94: return koUml;
      case 0x81: return kuUml;
      case 0xEF: return kDegree;
    }
  }

  return kBlank;   // unknown -> blank pixel column
}

// Read a glyph from PROGMEM into a RAM buffer of length 5.
inline void readGlyph(const uint8_t* progmemGlyph, uint8_t out[5]) {
  for (uint8_t i = 0; i < 5; ++i) {
    out[i] = pgm_read_byte(progmemGlyph + i);
  }
}

